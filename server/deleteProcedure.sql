CREATE OR REPLACE PROCEDURE DATA_DELETE
(
	PID DATASET.ID%TYPE
)
AS

BEGIN
	 DELETE FROM DATASET WHERE ID = PID;
	 COMMIT;

END;
/

DECLARE
	TID DATASET.ID%TYPE;
	NEGATIVE_VALUE EXCEPTION;

BEGIN

	TID := &ID;
	
	dbms_output.put_line(TID);


	IF TID<0 THEN
		RAISE NEGATIVE_VALUE;
	END IF;

	DATA_DELETE(TID);


	COMMIT;

	EXCEPTION
		WHEN NEGATIVE_VALUE THEN
			DBMS_OUTPUT.PUT_LINE('NEGATIVE VALUE');

END;
/
